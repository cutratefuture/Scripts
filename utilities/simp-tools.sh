#!/bin/bash
# Simple Tools v1.0.1
# mc (midnight commander) is a CLI GUI file manager
# tree is good for checking dirs in a nice visual format
# neofetch is also a must for a neat compact system stat
# htop is a color enhanced version of the top cmd
# net-tools various cli tools, notably for ifconfig. 
sudo apt update && sudo apt install mc tree neofetch htop net-tools -y